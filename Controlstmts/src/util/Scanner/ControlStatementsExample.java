package util.Scanner;

public class ControlStatementsExample {
	public static void main(String[] args) {
		//Forloop
		for(int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
		//While loop
	    int i = 1;

        while(i <= 5) {
            System.out.println(i);
            i++;
        }
        //d0 while
     
        do {
            System.out.println(i);
            i++;
        } while(i <= 5);

}
}
